    private void Btn_SimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_SimpanActionPerformed
        String id = idb.getText();
        String nama = nb.getText();
        String jenis = jb.getText();
        String jumlah = jmb.getText();
        String satuan = sb.getText();
        String harga = hb.getText();
        
        db.aksi("INSERT INTO barang VALUES ('" + id + "','" + nama + "','"+jenis+"','"+jumlah+"','"+harga+"','"+satuan+"')");
         model.setRowCount(0);
         tb_b.setModel(model);
         aturBariss();
        pn_konten_Barang.removeAll();
        pn_konten_Barang.repaint();
        pn_konten_Barang.revalidate();
        
        pn_konten_Barang.add(Tbl_Barang_Katalog);
        pn_konten_Barang.repaint();
        pn_konten_Barang.revalidate();
    }//GEN-LAST:event_Btn_SimpanActionPerformed

