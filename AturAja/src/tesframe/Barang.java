     public void aturBariss(){
        ResultSet hasil = db.ambilData("SELECT * FROM barang");
        try {
            while(hasil.next()) {
                model.addRow(new Object[]{hasil.getString("idbarang"), hasil.getString("nama_barang"),hasil.getString("jenis_barang"),hasil.getString("jumlah_barang"),hasil.getString("harga"),hasil.getString("satuan")});
                
            }
             tb_b.setModel(model);
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
